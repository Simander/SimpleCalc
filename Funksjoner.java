//Enkle kalkulatorfunksjoner
public class Funksjoner
{
	Funksjoner()
	{
		
	}
	//metode for 책 addere to verdier
	public float addisjon(float x, float y)
	{
		float sum = x + y;
		return sum;
	}
	//metode for 책 subtrahere to verdier
	public float subtraksjon(float x, float y)
	{
		float sum = x - y;
		return sum;
	}
	//metode for 책 multiplisere to verdier
	public float multiplikasjon(float x, float y)
	{
		float sum = x * y;
		return sum;
	}
	//metode for 책 dividere to verdier
	public float divisjon(float x, float y)
	{
		float sum = x / y;
		return sum;
	}
}
